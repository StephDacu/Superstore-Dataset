-- Creating a table for the superstore dataset
CREATE TABLE superstore (
	ship_mode VARCHAR(100),
	segment VARCHAR(50),
	country VARCHAR(50),
	city VARCHAR(50),
	state VARCHAR(50),
	postal_code INT,
	region VARCHAR(50),
	category VARCHAR(50),
	sub_category VARCHAR (50),
	sales DEC,
	quantity INT,
	discount DEC,
	profit DEC
);
